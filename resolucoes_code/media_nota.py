# 5 - Calculando Média de Notas 📚
# Vamos pedir para o usuário digitar as três notas 📝
print("Olá! Vamos calcular a sua média! 🤓")
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))
nota3 = float(input("Digite a terceira nota: "))

# Agora, usamos os operadores aritméticos para somar e dividir ✨
media = (nota1 + nota2 + nota3) / 3

# E finalmente mostramos o resultado na tela! 🏆
print(f"A média das suas notas é: {media:.2f} 🎈")
